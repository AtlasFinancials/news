def duration_ms(mp3_path):
    result = subprocess.run(
        ["ffprobe", "-v", "error", "-show_entries", "format=duration",
         "-of", "default=nw=1:nk=1", mp3_path],
        capture_output=True, text=True, check=True,
    )
    return int(float(result.stdout.strip()) * 1000)


def format_time(ms):
    s = ms // 1000
    return f"{s//60:d}:{s%60:02d}"


def main():
    client = texttospeech.TextToSpeechClient()

    section_files = []
    chapters = []
    cursor_ms = 0

    for i, (sid, title, text) in enumerate(SECTIONS):
        part_path = os.path.join(WORK_DIR, f"{i:02d}_{sid}.mp3")
        print(f"[{i+1}/{len(SECTIONS)}] {title}...")
        synth_section(client, text, part_path)
        dur = duration_ms(part_path)
        chapters.append({
            "id": sid,
            "title": title,
            "start_ms": cursor_ms,
            "end_ms": cursor_ms + dur,
            "start_label": format_time(cursor_ms),
        })
        cursor_ms += dur
        section_files.append(part_path)

    concat_list = os.path.join(WORK_DIR, "concat.txt")
    with open(concat_list, "w") as f:
        for p in section_files:
            f.write(f"file '{p}'\n")
    subprocess.run(
        ["ffmpeg", "-y", "-f", "concat", "-safe", "0", "-i", concat_list,
         "-c", "copy", FINAL_MP3],
        check=True, capture_output=True,
    )

    try:
        tags = ID3(FINAL_MP3)
    except Exception:
        tags = ID3()
    tags.delall("CHAP")
    tags.delall("CTOC")
    for ch in chapters:
        tags.add(CHAP(
            element_id=ch["id"],
            start_time=ch["start_ms"],
            end_time=ch["end_ms"],
            start_offset=0xFFFFFFFF,
            end_offset=0xFFFFFFFF,
            sub_frames=[TIT2(encoding=3, text=[ch["title"]])],
        ))
    tags.add(CTOC(
        element_id="toc",
        flags=CTOCFlags.TOP_LEVEL | CTOCFlags.ORDERED,
        child_element_ids=[ch["id"] for ch in chapters],
        sub_frames=[TIT2(encoding=3, text=["目次"])],
    ))
    tags.add(TIT2(encoding=3, text=["Atlas Morning Brief 2026-04-21"]))
    tags.save(FINAL_MP3)

    narration_backup = os.path.join(WORK_DIR, "narration_only_backup.mp3")
    subprocess.run(["cp", FINAL_MP3, narration_backup], check=True)

    total_ms = cursor_ms
    with open(CHAPTERS_JSON, "w", encoding="utf-8") as f:
        json.dump({
            "date": "2026-04-21",
            "total_ms": total_ms,
            "total_label": format_time(total_ms),
            "chapters": chapters,
        }, f, ensure_ascii=False, indent=2)

    print(f"\n完成: {FINAL_MP3}")
    print(f"  全長: {format_time(total_ms)}")
    print(f"  サイズ: {os.path.getsize(FINAL_MP3)/1024/1024:.1f} MB")
    print(f"  チャプター数: {len(chapters)}")
    print(f"  バックアップ: {narration_backup}")


if __name__ == "__main__":
    main()
